package com.aldrin.billing.model;

import com.aldrin.billing.util.EncryptPassword;


/**
 *
 * @author ALDRIN B. C.
 */

public class User {

    private Long id;
    private String firstname;
    private String surname;
    private String username;
    private String password;
    private java.sql.Timestamp inActiveAt;
    private byte[] photo;
    
    private Role role;

    public User() {

    }

    /**
     * @return the password
     */
    public String getPassword() {
        return password;
    }

    /**
     * @param password the password to set
     */
    public void setPassword(String password) {
        try{
           this.password = new EncryptPassword(password).toString();
        }catch(Exception e){
            e.printStackTrace();
        }  
    }

    /**
     * @return the id
     */
    public Long getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(Long id) {
        this.id = id;
    }

    /**
     * @return the firstname
     */
    public String getFirstname() {
        return firstname;
    }

    /**
     * @param firstname the firstname to set
     */
    public void setFirstname(String firstname) {
        this.firstname = firstname;
    }

    /**
     * @return the surname
     */
    public String getSurname() {
        return surname;
    }

    /**
     * @param surname the surname to set
     */
    public void setSurname(String surname) {
        this.surname = surname;
    }

    /**
     * @return the username
     */
    public String getUsername() {
        return username;
    }

    /**
     * @param username the username to set
     */
    public void setUsername(String username) {
        this.username = username;
    }

    /**
     * @return the inActiveAt
     */
    public java.sql.Timestamp getInActiveAt() {
        return inActiveAt;
    }

    /**
     * @param inActiveAt the inActiveAt to set
     */
    public void setInActiveAt(java.sql.Timestamp inActiveAt) {
        this.inActiveAt = inActiveAt;
    }

    /**
     * @return the photo
     */
    public byte[] getPhoto() {
        return photo;
    }

    /**
     * @param photo the photo to set
     */
    public void setPhoto(byte[] photo) {
        this.photo = photo;
    }

    /**
     * @return the role
     */
    public Role getRole() {
        return role;
    }

    /**
     * @param role the role to set
     */
    public void setRole(Role role) {
        this.role = role;
    }

}
